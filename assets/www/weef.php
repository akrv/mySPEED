<h3>WEEF - World Engineering Education Forum</h3>
<p align="justify">A  conversation on innovation should be inherent to Engineering.  Engineering development is closely tied to the search for innovative solutions to problems, which may in turn become innovations that will help optimize processes, services and products, and which, therefore, help improve the competitiveness and productivity of companies and countries.  Within this context, Engineering faculties have a commitment to the education of future engineers, and must promote the development of their innovation and entrepreneurship capacities. This educational process carried out in universities must be articulated with the corporate sector in order to make an effective contribution to the country’s innovation ecosystem.
    <p>In its most basic form, according to Cutler (2008), innovation means:</p>
          <blockquote>
            <p align="center"> “<em>transforming knowledge and ideas into a specific type of benefit”</em></p>
</blockquote>
          <p align="justify"> </p>
          <p align="justify">The World Bank defines innovation as the result of a capacity for generating and producing knowledge, a fundamental aspect for the development of nations.  In its latest report, the World Bank (2011, p.16) states:</p>
<blockquote>
<p align="center"> <em>“It is currently generally understood that sustainable development occurs when processes and results are locally owned and can be replicated and extended by local actors, including non-state actors, such as the civil society, private sector, service users and providers, academia and the citizens themselves.  Therefore, we need to use development-promotion instruments that will gather together the different actors for promoting changes in collaboration and leadership.”</em></p></blockquote>
<p> </p>
    <p>In the same manner, the OECD in its Oslo manual (2009) has defined innovation as:     </p><blockquote>
      <p align="center"><em> ”the implementation of a new or significantly improved product (good or service), or process, a new marketing method, or a new organizational method in business practices, workplace organization or external relations”.</em></p>
    </blockquote>
          <p>&nbsp;</p>
    <p align="justify">Implementation, according to the same source, must be interpreted in the following manner:</p>
          <blockquote>
            <p align="center"><em> “A new or improved product is implemented when it is introduced on the market.  New processes, marketing methods or organizational methods are implemented when they are brought into actual use in the firm’s operations”.</em></p>
    </blockquote>
          <p> </p>
    <p align="justify">Innovation in education is fundamental for accelerating country development. This is achieved through concrete changes in teaching and learning methods based on product generation (for example, creating/redesigning curriculums to adapt to local and global needs), processes (support for innovative techniques like virtual education or more effective teaching strategies), and relevant changes to the structures and behavior of educational institutions (for example, modes of admission, educational services promotion, modernization of physical classroom infrastructure, labs, libraries, spaces and times potentially included in their curriculums).  Educational innovation implies positive change that can be measured through processes and results.</p>
          <p align="justify">The International Federation for Engineering Education Socities – IFEES – and the Colombian Association of Engineering Faculties – ACOFI -, as part of their permanent work promoting quality in education, research and external relations in Engineering faculties, schools and programs around the world, will this year look at innovation topics that have an impact on the education of competitive and entrepreneurial engineers and on the development of teaching methods that will help educate engineers capable of innovating and generating significant changes in a global environment, as a working proposal for WEEF 2013.</p>
